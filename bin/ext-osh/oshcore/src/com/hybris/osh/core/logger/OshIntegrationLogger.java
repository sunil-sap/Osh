package com.hybris.osh.core.logger;

import org.apache.log4j.Logger;


/**
 * This logger class will take care of all the integration related logs
 */
public class OshIntegrationLogger
{
	private static final Logger LOG = Logger.getLogger(OshIntegrationLogger.class);

	public static void debug(final String msg)
	{
		LOG.debug(msg);
	}

	public static void info(final String msg)
	{
		LOG.info(msg);
	}

	public static void error(final Object msg)
	{
		LOG.error(msg);
	}

}
