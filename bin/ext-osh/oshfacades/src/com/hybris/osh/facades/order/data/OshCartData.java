package com.hybris.osh.facades.order.data;

import de.hybris.platform.commercefacades.order.data.CartData;
import de.hybris.platform.commercefacades.product.data.PriceData;


/**
 * 
 */
public class OshCartData extends CartData
{
	private String orderType;
	private PriceData totalAmount;
	private String voucherCode;
	private boolean shippingSurchargeStatus;

	/**
	 * @return the orderType
	 */
	public String getOrderType()
	{
		return orderType;
	}

	/**
	 * @return the voucherCode
	 */
	public String getVoucherCode()
	{
		return voucherCode;
	}

	/**
	 * @param voucherCode
	 *           the voucherCode to set
	 */
	public void setVoucherCode(final String voucherCode)
	{
		this.voucherCode = voucherCode;
	}

	/**
	 * @param orderType
	 *           the orderType to set
	 */
	public void setOrderType(final String orderType)
	{
		this.orderType = orderType;
	}

	/**
	 * @return the totalAmount
	 */
	public PriceData getTotalAmount()
	{
		return totalAmount;
	}

	/**
	 * @param totalAmount
	 *           the totalAmount to set
	 */
	public void setTotalAmount(final PriceData totalAmount)
	{
		this.totalAmount = totalAmount;
	}

	public boolean getShippingSurchargeStatus()
	{
		return shippingSurchargeStatus;
	}

	public void setShippingSurchargeStatus(final boolean shippingSurchargeStatus)
	{
		this.shippingSurchargeStatus = shippingSurchargeStatus;
	}
}
